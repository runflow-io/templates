import { Facebook, Instagram, Youtube } from "lucide-react";
import type { Platform } from "../lib/options";

type Props = {
  platform: Platform;
  className?: string;
};

/** Renders a recognizable mono brand mark for each Platform.
 *  Mixed source: lucide-react for the brands they cover, inline Simple Icons
 *  paths (CC0) for the ones they don't (TikTok, Pinterest, Amazon, Shopify). */
export function BrandIcon({ platform, className = "w-4 h-4" }: Props) {
  switch (platform) {
    case "tiktok":
      return <TikTokSvg className={className} />;
    case "instagram_feed":
    case "instagram_reel":
      return <Instagram className={className} />;
    case "facebook_feed":
      return <Facebook className={className} />;
    case "youtube":
    case "youtube_shorts":
      return <Youtube className={className} />;
    case "pinterest":
      return <PinterestSvg className={className} />;
    case "amazon_main":
      return <AmazonSvg className={className} />;
    case "shopify_pdp":
      return <ShopifySvg className={className} />;
    default:
      return null;
  }
}

